// Generate compound words - สร้างคำประสม
export const generateCompoundWords = () => {
  const compounds: string[] = [];
  
  // Generate prefix + root combinations
  laoSyllables.prefixes.forEach(prefix => {
    laoSyllables.roots.forEach(root => {
      compounds.push(prefix + root);
    });
  });
  
  // Generate root + suffix combinations
  laoSyllables.roots.forEach(root => {
    laoSyllables.suffixes.forEach(suffix => {
      compounds.push(root + suffix);
    });
  });
  
  return compounds.slice(0, 200); // Limit to 200 compounds
};

